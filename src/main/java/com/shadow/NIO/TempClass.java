package com.shadow.NIO;

public class TempClass {

    private int id;
    private String name;

    public TempClass() {
    }

    public TempClass(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
